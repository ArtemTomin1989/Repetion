function operacii(a, b) {
  let suma = a + b;
  let vidnimannya = a - b;
  let mnozhennya = a * b;
  let dilennya = a / b;

  console.log(`Сума ${a} і ${b} дорівнює ${suma}`);
  console.log(`Різниця ${a} і ${b} дорівнює ${vidnimannya}`);
  console.log(`Добуток ${a} і ${b} дорівнює ${mnozhennya}`);
  console.log(`Частка ${a} і ${b} дорівнює ${dilennya}`);
}

operacii(10, 5);
