function dodavannya(a, b) {
  console.log(`Сума чисел ${a} і ${b} = ${a + b}`);
}
dodavannya(10, 5);

function vidnimannya(a, b) {
  console.log(`Різниця чисел ${a} і ${b} = ${a - b}`);
}

vidnimannya(10, 5);

function mnozhennya(a, b) {
  console.log(`Добуток чисел ${a} і ${b} = ${a * b}`);
}
mnozhennya(10, 5);

function dilennya(a, b) {
  console.log(`Частка чисел ${a} і ${b} = ${a / b}`);
}
dilennya(10, 5);
