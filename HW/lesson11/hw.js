function dodavannya(a, b) {
  let suma = a + b;
  console.log(`Сума ${a} і ${b} дорівнює ${suma}`);
}
dodavannya(10, 5);

function vidnimannya(a, b) {
  let vidnimannya = a - b;
  console.log(`Різниця ${a} і ${b} дорівнює ${vidnimannya}`);
}
vidnimannya(10, 5);

function mnozhennya(a, b) {
  let mnozhennya = a * b;
  console.log(`Добуток ${a} і ${b} дорівнює ${mnozhennya}`);
}
mnozhennya(10, 5);

function dilennya(a, b) {
  let dilennya = a / b;
  console.log(`Частка ${a} і ${b} дорівнює ${dilennya}`);
}
dilennya(10, 5);
