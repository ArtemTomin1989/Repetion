function dodavannya(a, b) {
  console.log(`Сума = ${a + b}`);
}
dodavannya(10, 5);

function vidnimannya(a, b) {
  console.log(`Різниця = ${a - b}`);
}

vidnimannya(10, 5);

function mnozhennya(a, b) {
  console.log(`Добуток = ${a * b}`);
}
mnozhennya(10, 5);

function dilennya(a, b) {
  console.log(`Частка = ${a / b}`);
}
dilennya(10, 5);
